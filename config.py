"""
config.py - Конфигурационный модуль бота

Назначение:
- Загрузка переменных окружения
- Хранение констант и настроек
- Валидация конфигурации

Использование:
- Все настройки бота хранятся здесь
- Изменение параметров только в этом файле
- .env файл для чувствительных данных (токены)
"""

import os
from dotenv import load_dotenv

# ==================== ЗАГРУЗКА ПЕРЕМЕННЫХ ОКРУЖЕНИЯ ====================
# Переменные из .env файла
load_dotenv()

# ==================== КОНФИГУРАЦИЯ БОТА ====================
# Токен бота 
PARSER_BOT_TOKEN = os.getenv('CopyParserWMSBot')
# ID целевого чата 
CHAT_ID = os.getenv('CHAT_ID')
# Название чата для информационных сообщений
CHAT_NAME = '[TEST] CopyParserWMSBot Sandbox'

# ==================== НАСТРОЙКИ ПАРСЕРА ====================
# Максимальное количество ID для парсинга из одного сообщения
# Защита от чрезмерно длинных сообщений
MAX_IDS_PER_MESSAGE = 100

# Интервал автоочистки данных в секундах (2 часа)
# Устаревшие данные удаляются автоматически
DATA_CLEANUP_INTERVAL = 7200

# Режим отладки (True/False)
# Включает/выключает детальный вывод отладочной информации
DEBUG_MODE = True

# ==================== РЕГУЛЯРНЫЕ ВЫРАЖЕНИЯ ====================
# Паттерн для поиска ID AX: "ЗП-37075" + 4 цифры
AX_ID_PATTERN = r'ID AX:\s*(ЗП-37075\d{4})'

# Паттерн для поиска ID WMS: "0000" + 6 цифр
WMS_ID_PATTERN = r'ID WMS:\s*(0000\d{6})'

# ==================== ВАЛИДАЦИЯ КОНФИГУРАЦИИ ====================
def validate_config():
    """
    Проверка корректности загруженной конфигурации
    
    Возвращает:
        bool: True если конфигурация валидна, False если есть ошибки
    
    Действия:
        1. Проверяет наличие обязательных переменных
        2. Выводит информационные сообщения
        3. Возвращает результат проверки
    """
    # Проверка обязательных переменных
    if not PARSER_BOT_TOKEN or not CHAT_ID:
        print("    Ошибка: Не найдены переменные в .env файле")
        print(f"   PARSER_BOT_TOKEN: {'[OK]' if PARSER_BOT_TOKEN else '[ERROR]'}")
        print(f"   CHAT_ID: {'[OK]' if CHAT_ID else '[ERROR]'}")
        return False
    
    # Успешная загрузка конфигурации
    print(f"   CONFIG_LOADED: [OK]")
    print(f"   PARSER_BOT_TOKEN: [OK]")
    print(f"   CHAT_ID: {CHAT_ID}")
    print(f"   DEBUG_MODE: {DEBUG_MODE}")
    return True